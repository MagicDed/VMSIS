// Введеный массив выводится, записывается задом наперед и выводится его новый вариант(через два массива)

#include <stdio.h>

int main()
{
	int ms[10];                        // Первый массив
	int m[10];                         // Второй массив
	for (int i = 0; i < 10; i++)       // Цикл для ввода цифр в первый массив
	{
		printf("Write a number: ");          // Вывод текста (напишите число)
		scanf_s("%d", &ms[i]);               // Ввод цифр в массив
	}
	printf("Array: ");
	for (int i = 0; i < 10; i++)       // Цикл для вывода массива
	{
		printf("%d ", ms[i]);                 // Вывод первого массива(не перевернутого)
	}
	for (int i = 0; i < 10; i++)       // Цикл для записи значений во второй массив
	{
		m[i] = ms[9 - i];                    // Запись массива во второй начиная с конца второго
	}
	printf("\nFlipped array: ");       // Выводит текст (перевернутый массив)
	for (int i = 0; i < 10; i++)       // Цикл для вывода массива
	{
		printf("%d ", m[i]);                 // Вывод второго массива(перевернутый)
	}
	return 0;
}